var str1:string="Welcome To TypeScript";
// charAt()
console.log(str1.charAt(0));  // W
console.log(str1.charAt(3));  // c
// concat()
var str2:string="Welcome";
var str3:string="To TypeScript";
console.log(str2.concat(str3));

// replace()
var str4:string="TypeScript Programming";
console.log(str4.replace('i','x'));
console.log(str4.replace('Type', 'Java'));
//split()
var str5:string="Welcome to TypeScript";
console.log(str5.split(" "));

// trim()
var str6:string="   Welcome to TypeScript   ";
console.log(str6.trim());
console.log(str6.trimLeft());
console.log(str6.trimRight());
// length()
var str7:string="Welcome to TypeScript";
console.log(str7.length);



